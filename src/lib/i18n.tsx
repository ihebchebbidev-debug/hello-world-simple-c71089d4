const I18nCtx = createContext<{ lang: Lang; setLang: (l: Lang) => void; t: (k: string, vars?: Record<string, string | number>) => string; dir: "ltr" | "rtl" }>({
  lang: "fr", setLang: () => {}, t: (k) => k, dir: "ltr",
});

export function I18nProvider({ children }: { children: ReactNode }) {
  // Always start with "fr" so SSR and first client render match.
  // The persisted/browser preference is applied after mount in an effect.
  const [lang, setLangState] = useState<Lang>("fr");

  const dir: "ltr" | "rtl" = lang === "ar" ? "rtl" : "ltr";

  useEffect(() => {
    try {
      const saved = localStorage.getItem(STORAGE_KEY) as Lang | null;
      if (saved && dicts[saved]) { setLangState(saved); return; }
      const nav = navigator.language?.slice(0, 2).toLowerCase();
      if (nav === "ar" || nav === "en") setLangState(nav as Lang);
    } catch {}
  }, []);

  useEffect(() => {
    if (typeof document === "undefined") return;
    document.documentElement.lang = lang;
    document.documentElement.dir = dir;
  }, [lang, dir]);

  const setLang = useCallback((l: Lang) => {
    setLangState(l);
    try { localStorage.setItem(STORAGE_KEY, l); } catch {}
  }, []);

  const t = useCallback((k: string, vars?: Record<string, string | number>) => {
    const v = (dicts[lang][k] ?? dicts.fr[k] ?? k) as string;
    if (!vars) return v;
    return Object.entries(vars).reduce((acc, [key, val]) => acc.replaceAll(`{${key}}`, String(val)), v);
  }, [lang]);

  const value = useMemo(() => ({ lang, setLang, t, dir }), [lang, setLang, t, dir]);
  return <I18nCtx.Provider value={value}>{children}</I18nCtx.Provider>;
}

export const useI18n = () => useContext(I18nCtx);

const LANGS: Record<Lang, { code: string; label: string }> = {
  fr: { code: "FR", label: "Français" },
  en: { code: "EN", label: "English" },
  ar: { code: "AR", label: "العربية" },
};

const LANG_ORDER: Lang[] = ["fr", "en", "ar"];

import flagFr from "@/assets/flag-fr.png";
import flagEn from "@/assets/flag-en.png";
import flagAr from "@/assets/flag-ar.png";

const FLAGS: Record<Lang, string> = { fr: flagFr, en: flagEn, ar: flagAr };

/**
 * Flag-only language switcher. Tunisian flag for Arabic.
 */
export function LanguageSwitcher({ className = "" }: { className?: string }) {
  const { lang, setLang } = useI18n();

  return (
    <div
      role="group"
      aria-label="Language"
      className={
        "inline-flex items-center gap-1 rounded-full border border-border/60 bg-background/60 p-1 backdrop-blur " +
        className
      }
    >
      {LANG_ORDER.map((code) => {
        const active = code === lang;
        return (
          <button
            key={code}
            type="button"
            onClick={() => setLang(code)}
            aria-label={LANGS[code].label}
            aria-pressed={active}
            title={LANGS[code].label}
            className={
              "relative inline-flex h-7 w-7 items-center justify-center overflow-hidden rounded-full transition hover:scale-105 " +
              (active
                ? "ring-2 ring-primary ring-offset-1 ring-offset-background opacity-100"
                : "opacity-55 hover:opacity-100")
            }
          >
            <img
              src={FLAGS[code]}
              alt={LANGS[code].label}
              width={28}
              height={28}
              loading="lazy"
              className="h-full w-full object-cover"
            />
          </button>
        );
      })}
    </div>
  );
}
